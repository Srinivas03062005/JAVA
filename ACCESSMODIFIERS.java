/*
  Private - Access level only within the class. 
  Cannot be accessed outside the Class

  Default - Access level only within the package. 
  Cannot be accessed outside the package

  Protected - Access level within the package.and
   outside the package only through child class.

  Public - Access level is everywhere i.e. within 
  the class,outside the class and within the package
  and outside the package.
  
 */